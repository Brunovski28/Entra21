package E04Circulo;

public class Circulo {
	double raio1;
	double raio2;
	double raio3;
	double raio4;
	double raio5;
	double area = Math.PI*raio1*raio2*raio3*raio4*raio5;
	public Circulo(double raio1, double raio2, double raio3, double raio4, double raio5, double area) {
		
		this.raio1 = raio1;
		this.raio2 = raio2;
		this.raio3 = raio3;
		this.raio4 = raio4;
		this.raio5 = raio5;
		this.area = area;
	} 
	
}
