package E08ImpostoDeRenda;

public class ImpostoRenda {
	double renda;
	String nome;
	String cpf;
	String uf;
	double receba;
	public ImpostoRenda(double renda, String nome, String cpf, String uf, double receba) {
		
		this.renda = renda;
		this.nome = nome;
		this.cpf = cpf;
		this.uf = uf;
		this.receba = receba;
	}
	
}
