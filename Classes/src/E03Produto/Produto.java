package E03Produto;

public class Produto {
	String nome;
	String descricao;
	double precounitario;
	int desconto;
	public Produto(String nome, String descricao, double precounitario, int desconto) {
		this.nome = nome;
		this.descricao = descricao;
		this.precounitario = precounitario;
		this.desconto = desconto;
	}
	
}
