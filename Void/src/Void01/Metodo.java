package Void01;

public class Metodo {
	public String fds(int numero) {
		if (numero == 1) {
			return ("Verão");
		} else if (numero == 2) {
			return ("Inverno");
		} else if (numero == 3) {
			return ("Primavera");
		} else if (numero == 4) {
			return ("Outono");
		} else {
			return ("Número errado!");
		}
		
	}
}
