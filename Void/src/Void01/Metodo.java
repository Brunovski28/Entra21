package Void01;

public class Metodo {
	public String fds(int numero) {
		if (numero == 1) {
			return ("Verão tá potente");
		} else if (numero == 2) {
			return ("Inverno, que cu");
		} else if (numero == 3) {
			return ("Primavera, que cu");
		} else if (numero == 4) {
			return ("Outono, que cu");
		} else {
			return ("Número errado!");
		}
		
	}
}
